// Helper: get team members for a project
export function getProjectMembers(projectId: ProjectId): TeamMember[] {
  return TEAM_MEMBERS.filter((m) => m.projects.includes(projectId))
}

// Helper: get events for a project (optionally filtered by date)
export function getProjectEvents(projectId: ProjectId, dateFilter?: string): CalendarEvent[] {
  return CALENDAR_EVENTS.filter(
    (e) => e.projectId === projectId && (!dateFilter || e.date === dateFilter)
  )
}

// Helper: get tasks for a project, optionally by status
export function getProjectTasks(projectId: ProjectId, status?: string): Task[] {
  return TASKS.filter(
    (t) => t.projectId === projectId && (!status || t.status === status)
  )
}

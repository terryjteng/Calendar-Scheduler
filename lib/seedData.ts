// Week anchor: Monday 2026-05-04
const WEEK_START = '2026-05-04'

function weekDay(offsetDays: number): string {
  const d = new Date(WEEK_START)
  d.setDate(d.getDate() + offsetDays)
  return d.toISOString().split('T')[0]
}

